DATA 	SEGMENT
ASC DB 13,0,12 DUP(0)
DAT 	DB 6 DUP(0)
DATNUM 	DB 0
MULNUM2	DB 10H
CARRY   DB 0
RESULT 	DB 7 DUP(0) 
OUTBUF DB 14 DUP(30),'$'
DATA 	ENDS
STACK 	SEGMENT STACK
		DW 100 DUP (?) 
TOP 	LABEL WORD
STACK 	ENDS
CODE 	SEGMENT
ASSUME 	CS:CODE,DS:DATA,SS:STACK
START: 	MOV AX,DATA
		MOV DS,AX
		MOV AX,STACK
		MOV SS, AX
		MOV SP,OFFSET TOP
		LEA DX,ASC ;键盘输入
		MOV AH,0AH
		INT 21H
		MOV DL,10 ;光标下移
		MOV AH,2
		INT 21H 
		CALL TRAN
		LEA SI,DAT
		LEA DI,RESULT
		XOR CX,CX
		MOV CL,DATNUM
		ADD SI,CX
		DEC SI
		ADD DI,CX
LP1:	CALL DMULBLL
		DEC DI
		DEC SI
		LOOP LP1
		MOV BL,CARRY
		MOV [DI],BL
		CALL OUTPUT
		MOV AH,4CH
		INT 21H
		
DMULBLL  PROC NEAR
		PUSH AX
		PUSH DX
		PUSH CX
		MOV BL,[SI]
		XOR DX,DX
		ADD DL,CARRY
LP2:	CLC
		ADD DL,MULNUM2
		MOV AL,DL
		DAA
		MOV DL,AL
		ADC DH,0
		DEC BL
		MOV AL,BL
		DAS
		MOV BL,AL
		CMP BL,0
		JA LP2
		MOV [DI],DL
		MOV CARRY,DH
		POP CX
		POP DX
		POP AX
		RET
DMULBLL	ENDP

TRAN  PROC NEAR
		PUSH AX
		PUSH DX
		PUSH CX
		LEA SI,ASC
		INC SI
		XOR CX,CX
		MOV CL,[SI]
		MOV DL,DATNUM
		INC SI
		LEA DI,DAT
LLPP1:	MOV AX,[SI]
		DEC CL
		SUB AL,30H
		SUB AH,30H
		MOV BH,AH
		MOV BL,10H
		MUL BL
		ADD AL,BH
		MOV [DI],AL
		ADD SI,2
		INC DI
		INC DX
		LOOP LLPP1
		MOV DATNUM,DL
		POP CX
		POP DX
		POP AX
		RET
TRAN	ENDP


OUTPUT  PROC NEAR
		PUSH AX
		PUSH DX
		PUSH CX
		MOV CX,7
		LEA DI,OUTBUF
		LEA SI,RESULT
LLPP:	MOV AL,[SI]
		MOV AH,AL
		MOV BX,CX
		MOV CL,4
		ROR AL,CL
		MOV CX,BX
		AND AH,0FH
		AND AL,0FH
		ADD AL,30H
		CMP AL,39H
		JBE HEX1
		ADD AL,07H
HEX1:	ADD AH,30H
		CMP AH,39H
		JBE HEX2
		ADD AH,07H
HEX2:	MOV [DI],AX
		ADD DI,2
		INC SI
		LOOP LLPP
		LEA DX,OUTBUF
		MOV AH,9
		INT 21H
		POP CX
		POP DX
		POP AX
		RET
OUTPUT	ENDP		
		
CODE 	ENDS
END 	START
